#include<stdlib.h>

void	ft_bzero(void *s, size_t n)
{
	size_t		i;
	unsigned char	*b;

	i = 0;
	b = (unsigned char *)s;
	while (i < n)
	{
		b[i] = 0;
		i++;
	}
}
